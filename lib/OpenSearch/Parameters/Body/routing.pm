package OpenSearch::Parameters::Body::routing;
use Moose::Role;

has "routing" => (
  is            => "rw",
  isa           => "Str",
  documentation => {
    encode_func => undef,
    required    => undef,
  }
);

around "routing" => sub {
  my $orig = shift;
  my $self = shift;

  if (@_) {
    $self->$orig(@_);
    return ($self);
  }
  return ( $self->$orig );
};

1;
