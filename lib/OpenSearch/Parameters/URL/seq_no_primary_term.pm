
  package OpenSearch::Parameters::URL::seq_no_primary_term;
  use Moose::Role;

  has "seq_no_primary_term" => (
    is => "rw",
    isa => "Bool",
    documentation => {
      encode_func => "encode_bool",
      required => undef,
    }
  );

  around "seq_no_primary_term" => sub {
    my $orig = shift;
    my $self = shift;

    if(@_) {
      $self->$orig(@_);
      return($self);
    }
    return($self->$orig);
  };

1;
