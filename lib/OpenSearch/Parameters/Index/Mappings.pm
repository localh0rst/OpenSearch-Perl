around [
  qw/index properties dynamic allow_no_indices ignore_unavailable ignore_malformed cluster_manager_timeout write_index_only/
] => sub {
  my $orig = shift;
  my $self = shift;

  if (@_) {
    $self->$orig(@_);
    return ($self);
  }
  return ( $self->$orig );
};

1;
